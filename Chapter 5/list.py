# list 

# list is a: Ordered Collection of Items

# That can be:
# - Of any data type (strings, integers, floats, etc.)
# - Mutable (modifiable)
# - Dynamic (grow or shrink as elements are added/removed)

# Key Characteristics:
# 1. Indexed (access elements using square brackets [] and index)
# 2. Ordered (elements have a specific order)
# 3. Mutable (elements can be modified)
# 4. Dynamic (size can change)

#Here are examples of different types of lists in Python:

# 1. Empty List

my_list = []
print(my_list)


# 2. Homogeneous List (all elements of the same type)

numbers = [1, 2, 3, 4, 5]
print(numbers)


# 3. Heterogeneous List (elements of different types)

mixed = ["apple", 42, 3.14, True]
print(mixed)


# 4. Integer List

integers = [1, 2, 3, 4, 5]
print(integers)


# 5. Float List

floats = [3.14, 2.71, 1.61]
print(float)


# 6. String List

fruits = ["apple", "banana", "cherry"]
print(fruits)


# 7. Boolean List

bools = [True, False, True, False]
print(bools)


# 8. Nested List (list inside a list)

matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(matrix)


# 9. Multidimensional List (list of lists of lists)

cube = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]
print(cube)
