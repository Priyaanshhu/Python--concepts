# add data into list

# The append() method in Python lists adds a single element to the end of the list.

# Syntax:
# list_name.append(element)

# Key Points:
# 1. Only one element can be added at a time
# 2. The element can be of any data type (string, integer, float, etc.)
# 3. The element is added to the end of the list

fruits = ["apple", "banana"]
fruits.append("cherry")
print(fruits)  
