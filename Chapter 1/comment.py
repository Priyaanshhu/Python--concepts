# comment

# a comment is a line of code that is ignored by the interpreter. Comments are used to:

# 1. Add notes or explanations to the code
# 2. Temporarily disable code without deleting it
# 3. Improve code readability

# Best practices:
# - Use comments to explain complex code or logic
# - Keep comments concise and clear
# - Avoid commenting obvious code
# - Use comments to disable code temporarily instead of deleting it


# There are two types of comments in Python:

# Single-line comment : "#"
# This is a single-line comment

# Multi-line comment : (''' or """) 
'''
This is a multi-line comment
that spans multiple lines
'''

"""
This is another multi-line comment
that spans multiple lines
"""
