# python as calculator 

# using python as a calculator by putting number and Operator in print() function


# Addition 
print(3+9)

# Substraction
print(3-9)

# Multiplication 
print(3*9)

# Division 
print(3/9)

# Floor Division (Divide two number and return largest whole number less than or equal to result)
print(3//5)

# Modules (give remainder of Division)
print(3%9)

# Exponentiation (number to a power)
print(2**3)
