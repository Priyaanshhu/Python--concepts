# Escape sequences

# Escape sequences are special sequences of characters that represent non-printable characters, such as:

# Here are the escape sequences :

# Single quote: "\'"
print('Heyy!! I\'m a single quote')

# Double quote: "\"" 
print("Heyy!! I'm a \"double\" quote")

# Backslash: "\\" 
print("Heyy!! I'm a \\backlash\\")

# New line: "\n"
print("Heyy!! I'm a \nnew line")

# Tab: "\t" 
print("Heyy!! I'm a \t tab")

# Backspace: "\b" 
print("Heyy!!\b I'm a backspace")
