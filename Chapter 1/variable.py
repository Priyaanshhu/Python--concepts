# variable

# a variable is a name given to a value. Variables are used to store and manipulate data in a program.

# Rules of Variables in Python:

# 1. Variable Names: Variable names can consist of letters (a-z, A-Z), digits (0-9), and underscores (_).
# 2. Case Sensitivity: Variable names are case-sensitive, meaning `x` and `X` are different variables.
# 3. Unique Names: Variable names must be unique within their scope.
# 4. Assignment: Variables are assigned using the assignment operator (=).
# 5. Data Type: Variables can hold any data type, including strings, numbers, lists, dictionaries, etc.
# 6. Reassignment: Variables can be reassigned to a new value.
# 7. Scope: Variables have a scope, which defines their visibility and accessibility.

# Best Practices:

# 1. Use meaningful names: Choose variable names that describe their purpose.
# 2. Use consistent naming conventions: Follow a consistent naming convention throughout your code.
# 3. Avoid reserved words: Avoid using Python reserved words as variable names.
# 4. Use underscores: Use underscores to separate words in variable names.

# Types of Variables:

# 1. Local Variables: Defined within a function or block.
# 2. Global Variables: Defined outside of functions or blocks.
# 3. Instance Variables: Defined within a class.


