# string indexing 

# String indexing in Python allows you to access individual characters within a string.

# Indexing Syntax: string[index]

# Index Types:
# 1. Positive Indexing: Starts from 0 (beginning of the string)
# 2. Negative Indexing: Starts from -1 (end of the string)


# Example: 

my_string = "Pomegranate"

# string   positive index   negative index

# P             0                -11
# o             1                -10
# m             2                -9
# e             3                -8
# g             4                -7
# r             5                -6
# a             6                -5
# n             7                -4
# a             8                -3
# t             9                -2
# e             10               -1

# getting positive index (from start)
print(my_string[0])  
print(my_string[1])
print(my_string[2])
print(my_string[3])
print(my_string[4])
print(my_string[5])
print(my_string[6])
print(my_string[7])
print(my_string[8])
print(my_string[9])
print(my_string[10])

# getting negative index (from end)
print(my_string[-1]) 
print(my_string[-2])
print(my_string[-3])
print(my_string[-4])
print(my_string[-5])
print(my_string[-6])
print(my_string[-7])
print(my_string[-8])
print(my_string[-9])
