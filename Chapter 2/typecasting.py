# typecasting

# Typecasting in Python is the process of converting a value from one data type to another. 

# Here are some common typecasting methods:

# 1. int(): Converts a value to an integer.

num1 = int("5")
num2 = int("10")
result = num1 + num2
print(result)

# 2. float(): Converts a value to a floating-point number.

num1 = float(55)
print(num1)

# 3. str(): Converts a value to a string.

num1 = str(5)
num2 = str(5)
print(num1 + num2)

# Note that some typecasting methods may raise errors if the conversion is not possible. For example, 'int("hello")' will raise a 'ValueError'

# Example : 
num1 = int(input("Enter your number 1 : "))
num2 = int(input("Enter your number 2 : "))
sum = (num1 + num2)
print(sum)
