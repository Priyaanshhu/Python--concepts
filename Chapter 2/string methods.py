# string methods 

# String methods are built-in functions that allow you to manipulate and transform strings.

# Common String Methods:

my_string = "poMEgRANATE fruITS"

# 1. lower(): Converts to lowercase.
print(my_string.lower())

# 2. upper(): Converts to uppercase.
print(my_string.upper())

# 3. title(): Converts to title case.
print(my_string.title())

# 4. count(): Returns count of substring occurrences.
print(my_string.count('r'))
print(my_string.count('T'))

# 5. len() :  returns the length of a string, which is the number of characters it contains.
print(len(my_string))
