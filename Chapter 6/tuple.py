# Tuples

# A tuple is an immutable, ordered collection of values.

# Characteristics:
# 1. Immutable (cannot be changed after creation)
# 2. Ordered (elements have a specific order)
# 3. Indexed (elements can be accessed by index)
# 4. Can contain duplicate values

# Creating Tuples:
# 1. Using parentheses ()
# 2. Using tuple() function
# 3. Using comma-separated values


# Example 1:
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)  # Output: (1, 2, 3, 4, 5)

# Example 2:
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple[0])  # Output: 1
print(my_tuple[-1])  # Output: 5
