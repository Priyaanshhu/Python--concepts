# else statement

# The else statement in Python is used to specify a block of code to execute when the if condition is false.

# Syntax:
# if condition:
     # code to execute if true
# else:
     # code to execute if false


age = int(input("Enter you age : "))

if age >= 18: 
    print(f"You're age is {age} you're an adult")
else:
    print(f"You're age is {age} you're a child")  
