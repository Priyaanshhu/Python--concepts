# while loop 

# The while loop in Python is used to execute a block of code repeatedly as long as a certain condition is true.

# Syntax:
# while condition:
    # code to execute


# Example 1: 

i = 1
while i <= 10: 
  print(f"Hello world {i}")
  i = i + 1

# Example 2: 

num = int(input("Enter number to get his table : "))

i = 1 
while  i <= 10:
  print(f"{num} x {i} = {num * i}")
  i = i + 1
