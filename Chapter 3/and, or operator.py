# and, or operator 

# 'and' and 'or' are logical operators used to combine conditional statements.


# And Operator ('and'):
# Returns 'True' if both conditions are 'True'
# Syntax: condition1 and condition2

name = "abc"
age = 20

if name == "abc" and age == 20:
    print("Condition is True")
else:
    print("Condition is false")  
    
        
# Or Operator ('or'):
# Returns 'True' if either(anyone) condition is 'True'
# Syntax: condition1 or condition2

name = "abc"
age = 21

if name == "abc" or age == 30:
    print("Condition is True")
else:
    print("Condition is false")  
